package base.base;

public class Switch {

    public static void main(String[] args) {
        char num = 'b';
        switch (num) {
            case 'a':
                System.out.println("123");
                break;
            case 'b':
                System.out.println("12345");
                break;
            case 'c':
                System.out.println("123456");
                break;
            case 'd':
                System.out.println("1234567");
                break;
            default:
                System.out.println("12345678");
        }

    }

}
